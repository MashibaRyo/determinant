import numpy as np
import matplotlib.pyplot as plt

# Данные сигнала во временной области
signal_values = np.array([
    -1.000000, -2.000000, 3.000000, 3.000000, -3.000000, 0.000000, -1.000000, -2.000000,
    1.000000, 1.000000, 0.000000, 3.000000, -1.000000, 3.000000, -4.000000, 2.000000,
    -1.000000, 1.000000, 0.000000, -1.000000, -5.000000, 2.000000, -2.000000, -1.000000,
    -1.000000, 3.000000, 1.000000, -2.000000, -2.000000, 0.000000, 0.000000, 0.000000,
    -1.000000, 3.000000, -1.000000, -1.000000, -2.000000, -4.000000, -2.000000, 1.000000,
    1.000000, 1.000000, 2.000000, -1.000000, 3.000000, -3.000000, -1.000000, -2.000000,
    0.000000, -4.000000, 1.000000, -1.000000, 1.000000, -2.000000, 0.000000, -1.000000,
    -2.000000, -5.000000, 0.000000, -5.000000, 2.000000, 2.000000, -1.000000, 5.000000, 1.000000
])

# Выполняем преобразование Фурье
fft_values = np.fft.fft(signal_values)  # Быстрое преобразование Фурье
frequencies = np.fft.fftfreq(len(signal_values))  # Частоты

# Амплитудный спектр
amplitude_spectrum = np.abs(fft_values)

# Визуализация
plt.figure(figsize=(12, 6))

# График сигнала во временной области
plt.subplot(2, 1, 1)
plt.plot(signal_values, label='Сигнал во временной области', color='blue')
plt.title('Сигнал во временной области', fontsize=14)
plt.xlabel('Отсчеты', fontsize=12)
plt.ylabel('Амплитуда', fontsize=12)
plt.grid(True, linestyle='--', alpha=0.7)
plt.legend()

# График спектра сигнала
plt.subplot(2, 1, 2)
plt.plot(frequencies, amplitude_spectrum, label='Спектр сигнала', color='red')
plt.title('Спектр сигнала (преобразование Фурье)', fontsize=14)
plt.xlabel('Частота (Гц)', fontsize=12)
plt.ylabel('Амплитуда', fontsize=12)
plt.grid(True, linestyle='--', alpha=0.7)
plt.legend()

plt.tight_layout()
plt.show()